public class PP2 {
    /**
     * Write a program that prints the outline of a house using tilde (~)
     * characters.
     * */

    public static void main(String[] args){
        System.out.println("    ~");
        System.out.println("   ~ ~");
        System.out.println("  ~   ~");
        System.out.println(" ~~~~~~~");
        System.out.println(" ~     ~");
        System.out.println(" ~     ~");
        System.out.println(" ~     ~");
        System.out.println("~~~~~~~~~");
    }
}
